#!/usr/bin/env python3
import matplotlib.pyplot as plt
import json
import argparse
from pathlib import Path

if __name__ == '__main__':
    parser = argparse.ArgumentParser(
        description='plot the benchmarks generated by the google/benchmark framework')
    parser.add_argument('jsonfile', metavar='F', type=str,
                        help='json file containing the benchmarks', required=True)

    filepath = Path(parser.jsonfile)
    if not filepath.is_file:
        raise ValueError("You must pass a json file to the program.")
